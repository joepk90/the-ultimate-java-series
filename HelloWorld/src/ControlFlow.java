public class ControlFlow {
    
    public static void main(String[] args) {
        ControlFlow.comparisonOperators();
    }

    public static void comparisonOperators() {
        int x = 1;
        int y = 1;
        System.out.println(x == y); // true
        System.out.println(x != y); // false
        System.out.println(x > y); // false
        System.out.println(x >= y); // true
        System.out.println(x < y); // false
        System.out.println(x <= y); // true
    }
}
